/*11. Calcular la suma de elementos positivos y negativos en un arreglo.*/

#include<iostream>
using namespace std;

int main(){
	int n;
	int numeros[n];
	cout<<"¿cuantos numeros desea digitar?"<<endl;
	cin>>n;
	
	for(int i=0; i<n; i++){
		cout<<"Digite el numero "<<i+1<<endl;
		cin>>numeros[n];
	}
	
	
	return 0;
} 
